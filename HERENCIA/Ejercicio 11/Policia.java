package Herencia.Ejercicio11;

public class Policia {
    private String grado;
    private String rango;

    public Policia(String grado, String rango) {
        this.grado = grado;
        this.rango = rango;
    }

    public String getGrado() {
        return grado;
    }

    public String getRango() {
        return rango;
    }
}
